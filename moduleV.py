from turtle import *

def draw_logo_1():

    bgcolor("black")
    speed(1)
    pensize(25)
    color("blue")
    hideturtle() 

    penup()  
    goto(-65, 75) 
    pendown() 

    right(65)
    forward(150)
    left(135)
    forward(150)

