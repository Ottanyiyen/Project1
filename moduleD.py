from turtle import *

def draw_logo_1():
    hideturtle()
    bgcolor('black')
    pencolor('white')
    fillcolor('white')

    penup()
    goto(-100, -120)
    pendown()

    begin_fill()
    setheading(90)
    forward(240)
    right(90)
    forward(90)
    circle(-122, 166)
    goto(-100, -120)
    end_fill()

    penup()
    fillcolor('black')
    pencolor('black')

    begin_fill()
    goto(-44, -50)
    pendown()
    setheading(90)
    forward(100)
    setheading(0)
    forward(50)
    circle(-50, 180)
    forward(50)
    end_fill()


