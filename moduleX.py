from turtle import *

def draw_logo_1():
    hideturtle()
    bgcolor('black')
    pencolor('white')
    fillcolor('white')
    right(90)

    up()
    goto(-80, 110)
    down()
    left(40)

    begin_fill()
    for _ in range(2):
        forward(270)
        left(50)
        forward(34)
        left(130)
    end_fill()

    up()
    goto(55, 110)
    down()
    left(140)

    begin_fill()
    for _ in range(2):
        forward(50)
        left(144)
        forward(200)
        left(36)
    end_fill()


